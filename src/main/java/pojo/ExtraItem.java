package pojo;

public class ExtraItem {
    private int violin;
    private int cap;

    public int getViolin() {
        return violin;
    }

    public void setViolin(int violin) {
        this.violin = violin;
    }

    public int getCap() {
        return cap;
    }

    public void setCap(int cap) {
        this.cap = cap;
    }
}